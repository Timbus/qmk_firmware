bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case FARROW:
      if (record->event.pressed) {
        // when keycode is pressed
        SEND_STRING("->");
      } else {
        // when keycode is released
      }
      break;
    case BARROW:
      if (record->event.pressed) {
        // when keycode is pressed
        SEND_STRING("<-");
      } else {
        // when keycode is released
      }
      break;
    case FAARROW:
      if (record->event.pressed) {
        // when keycode is pressed
        SEND_STRING("=>");
      } else {
        // when keycode is released
      }
      break;
    case BAARROW:
      if (record->event.pressed) {
        // when keycode is pressed
        SEND_STRING("<=");
      } else {
        // when keycode is released
      }
      break;
  }
  return true;
};